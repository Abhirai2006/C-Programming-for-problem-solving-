#include <stdio.h>

int main() {
    int decimal = 17;    // Decimal
    int octal = 017;      // Octal (base 8)
    int hexadecimal = 0x17; // Hexadecimal (base 16)

    printf("Decimal: %d\n", decimal);
    printf("Octal 021: %d\n", octal);
    printf("Hexadecimal 0x17: %d\n", hexadecimal);

    return 0;
}
